using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApplication1
{
    class Operators
    {

        /*
         *  XOR >> ^ >> Exclusive OR
         *  & | ^
         *  XNOR = !XOR
         */
        public static double GetPowser(int baseNumber, int powerNumber)
        {
            return Math.Pow(baseNumber, powerNumber);
        }
    }
}
